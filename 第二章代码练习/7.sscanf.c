#include<stdio.h>
int main(){
    char s[100] = "123 456 789";
    int a, b, c;
    sscanf(s, "%d%d%d", &a ,&b, &c);//sscanf从s字符串中读入数据传给...
    printf("a = %d, b = %d, c = %d\n", a, b, c);

    return 0;
}
