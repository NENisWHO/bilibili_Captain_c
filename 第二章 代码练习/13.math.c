#include<stdio.h>
#include<math.h>
#include<stdlib.h>


int main(){
    printf("pow(2, 3) = %lf\n", pow(2.0, 3.0));
    printf("sqrt(2) = %lf\n", sqrt(2));
    printf("ceil(4.01) = %lf\n", ceil(4.01));
    printf("floor(4.9) = %lf\n", floor(4.9));
    printf("abs(-67) = %d\n", abs(-67));
    printf("fabs(-67.13) = %lf\n", fabs(-67.13));
    printf("acos(-1) = %lf\n", acos(-1));


    return 0;
}
